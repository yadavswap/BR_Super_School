<p class="center">Our school has been serving the city for 75 years with great academic and non-academic excellence but
    the path of this achievement was not easy. It was way back in 1920 s where there was no high school in
    our taluka . It was very difficult for students to continue further education .With this intention the stone
    laid down as Shrikrishnna High school in Mr Khankhoje’s House in Dantoli and Mr.Baburaoji Mulmule
    was the headmaster of the school. The school got government recognition in 1940.with the help of Shri
    T.G.Kedar, the vice chancellor, Nagpur University, Nagpur and Adv.Mangalmurti. Without financial help
    of Shri Bapuji Mulmule ,it was difficult to run the school . Teachers like Shri Amrutroji Pande and Dr.S.V.
    Bobde offered offered their free services to grow this school. After three years Shri.Laxminarayanji
    Rathi,The Mayor Of the city started a new Municipal school named Municipal Agriculture and
    Commerce Highschool in Anandramji Paliwal’s House.But soon they found that it was difficult to run the
    two schools in the same city and after a long deliberation they decided to unite both the schools and
    renamed it after the name of the famous industrialist Mr.Banaradidas Ruiya who donated 22000 Rs at
    that time with the initiative of Mr.Madanlalji Lata known as Munimji.He was quite aware about the
    failure of Katol Highschool Katol due to insufficient fund. So he sought aid from Ruiya Trust where he
    was working as an accountant.
    This new school named Banarasidas Ruiya Highschool became active on 1
    August 1944 and the first headmaster was Baburaoji Mulmule and in 1945 Shri Marathe sir became the
    second headmaster but his regime was very short.After that Mr.P. Gorde and in1946 Mr. Appasaheb
    Bhandarkar became the Headmaster. In this very year This Institution got registered and the first
    managing committee was formed. Mr. Madanlalji Lata (The president), Shri. Ganpat Deshmukh (vice
    President), Shri Vallabhdasji Bhutada (secretory) Shri. Champalalji Tapadiya(treasurer) ,shri Tilokchandji
    Khona,Dr.Dev,Shri.Kisandasji Nabira,Shri Gulamhussainji Khoja,Shri Kashiraoji Deshmukh and Anandraoji
    Fiske were the members. Shri. K.B. Harjal became the headmaster in 1948 who went to England for
    studying English Teaching Method and brought a new fame to the school. His work was so eminent that
    the school is still known after his name.
    Within a short period the school began to prosper very fast and the number of
    students increased. Committee started hunting for new building with the help of Mr. Arjun Khemji Firm
    who donated their land for new building of the school with 10 rooms after that the gorgeous hands of
    the society started donating like Madanlalji Taori,Parvatibai Jodharajji Nabira and Shri Gulamhussainji
    Khoja.Shri Hussainbhai Maoji donated a godown and land to the school and Madhavraoji Deshmukh
    extended his hand for further construction. There was no ground for the school so again the efforts of
    the management committee proved fruitful and got the land of four acres from Krishnaji Raut .
    Balkrishnnaji Ghode donated some land for the ground.
    The secondary school started in1960 and higher secondary school began in 1965.SCC Board Examination
    centre was started in1956 and 4000 students were appeared in the exam from this centre. Different
    examinations like Rastrabhasha Prachar Samiti,Wardha, Sankrit examination of Bharatiya Vidya Bhavan
    Mumbai,Drawing Grade Examination and Vidharbh Sahitya Sangh,Nagpur were also conducted in the
    school.scout and guide stated in 1954 and now we have two wings of NCC also.</p>
    
